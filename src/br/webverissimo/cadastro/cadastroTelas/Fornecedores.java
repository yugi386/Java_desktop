// carrega todos os contatos do fornecedor.
 private void carregarContatosFornecedor(String condicao) throws SQLException {
    FornecedorDAO fornecedor = new FornecedorDAO();

    List<Map<String , String>> registros  = new ArrayList<Map<String,String>>();
    List<Map<String , String>> usuarios  = new ArrayList<Map<String,String>>();
    
    // a variavel registros fornece a lista com os contatos do fornecedor.
    String[] tabCampo = {"fornecedores_id","contatos_id"};
    registros = fornecedor.listarTabelasAssociadas("contatos_fornecedores",tabCampo,condicao);
         
    if (registros != null){
           // Modelo da lista
           DefaultListModel lista = new DefaultListModel();
           String[] tabCampo2 = {"id","nome"};   // campos da tabela usuarios
                   
           try {
               jLContatosIncluir.clearSelection();
               for(int ct=0;ct<registros.size();ct++) {
                   usuarios = fornecedor.listarTabelasAssociadas("usuarios",tabCampo2,"id="+registros.get(ct).get("contatos_id"));
                   lista.addElement(usuarios.get(0).get("nome"));
               }
               jLContatosIncluir.setModel(lista);

           } catch (Exception ex) {
               Logger.getLogger(Fornecedores.class.getName()).log(Level.SEVERE, null, ex.getMessage());
           }
    } else {
        limparContatosFornecedor();
    }
 }    
